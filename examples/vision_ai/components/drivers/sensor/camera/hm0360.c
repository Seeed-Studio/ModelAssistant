static void hm0360_power_init(void)
{
#ifdef CAMERA_ENABLE_PIN
	hx_drv_iomux_set_pmux(HM0360_EN_GPIO, 3);
	hx_drv_iomux_set_outvalue(HM0360_EN_GPIO, HM0360_EN_STATE);
	board_delay_ms(50);
#endif
	return;
}

static void hm0360_power_off(void)
{
#ifdef CAMERA_ENABLE_PIN
	hx_drv_iomux_set_pmux(HM0360_EN_GPIO, 3);
	hx_drv_iomux_set_outvalue(HM0360_EN_GPIO, 1 - HM0360_EN_STATE);
#endif
	return;
}

static int8_t hm0360_get_sensor_id(uint32_t *sensor_id)
{
	uint16_t reg_addr = 0x0000;
	uint8_t reg_value = 0x00;
	uint8_t retry_max_cnt = 3;

	reg_addr = 0x0000;

	// add the retry to wait for the camera startup
	for (uint8_t i = 0; i < retry_max_cnt; ++i)
	{
		if (hx_drv_cis_get_reg(reg_addr, &reg_value) != HX_CIS_NO_ERROR)
			continue;

		if (reg_value)
			break;
	}

	if (hx_drv_cis_get_reg(reg_addr, &reg_value) != HX_CIS_NO_ERROR)
	{
		LOGGER_WARNING("<%s><%d>get sensor id failed\n", __func__, __LINE__);
		return -EIO;
	}
	*sensor_id = reg_value << 8;

	reg_addr = 0x0001;
	if (hx_drv_cis_get_reg(reg_addr, &reg_value) != HX_CIS_NO_ERROR)
	{
		LOGGER_WARNING("<%s><%d>get sensor id failed\n", __func__, __LINE__);
		return -EIO;
	}
	*sensor_id = *sensor_id | reg_value;

	return 0;
}

static int8_t hm0360_set_output_size(uint16_t width, uint16_t height)
{
	HX_CIS_SensorSetting_t* sensor_cfg = NULL;
	uint16_t sensor_cfg_len;
	uint32_t result = DP_NO_ERROR;
	uint16_t start_x = 0;
	uint16_t start_y = 0;

	if(!width || !height)
	{
		LOGGER_ERROR("camera size param error,width:%d, height:%d\n",\
		width, height);
		return -EINVAL;
	}
	
	//chose the right config of different resolution
	if(width <= 160 && height <= 112)
	{
		sensor_cfg = sensor_hm0360_setting_160x120;
		sensor_cfg_len = HX_CIS_SIZE_N(sensor_hm0360_setting_160x120,
									HX_CIS_SensorSetting_t);
		start_x = (160 - width) / 2;
		start_y = (112 - height) / 2;
	}
	else if(width <= 320 && height <= 240)
	{
		sensor_cfg = sensor_hm0360_setting_320x240;
		sensor_cfg_len = HX_CIS_SIZE_N(sensor_hm0360_setting_320x240,
									HX_CIS_SensorSetting_t);
		start_x = (320 - width) / 2;
		start_y = (240 - height) / 2;
	}
	else if(width <= 640 && height <= 480)
	{
		sensor_cfg = sensor_hm0360_setting_640x480;
		sensor_cfg_len = HX_CIS_SIZE_N(sensor_hm0360_setting_640x480,
									HX_CIS_SensorSetting_t);
		start_x = (320 - width) / 2;
		start_y = (240 - height) / 2;
	}
	else
	{
		LOGGER_ERROR("Camera output size overflow w:%d h:%d\n", width, height);
		return -EINVAL;
	}

	datapath_set_roi_start_position(start_x, start_y);

	if(sensor_cfg)
	{
		result = hx_drv_cis_setRegTable(sensor_cfg, sensor_cfg_len);
		if(result)
			return -EINVAL;
	}

	return 0;
}

